import numpy as np

array = np.array([1, 2, 3, 4, 5])

sqrt_array = np.sqrt(array)    # Square root
print(sqrt_array)

exp_array = np.exp(array)      # Exponential
print(exp_array)

sin_array = np.sin(array)      # Sine
print(sin_array)

log_array = np.log(array)      # Natural logarithm
print(log_array)