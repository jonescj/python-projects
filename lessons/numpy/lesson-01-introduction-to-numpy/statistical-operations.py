import numpy as np

array = np.array([1, 2, 3, 4, 5])

mean = np.mean(array)  # Mean
print(mean)

std_dev = np.std(array) # Standard deviation
print(std_dev)

variance = np.var(array) # Variance
print(variance)

min_val = np.min(array)  # Minimum value
print(min_val)

max_val = np.max(array)  # Maximum value
print(max_val)